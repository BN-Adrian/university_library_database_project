CREATE TABLE premii_studenti (
    id_premiu NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_student NUMBER,
    luna NUMBER,
    anul NUMBER,
    data_acordarii DATE DEFAULT SYSDATE
);


CREATE TABLE log_imprumuturi (
    id_log NUMBER GENERATED BY DEFAULT AS IDENTITY,
    tip_activitate VARCHAR2(10),
    isbn VARCHAR2(20),
    id_utilizator NUMBER,
    data_operatie DATE,
    observatii VARCHAR2(255)
);

CREATE TABLE amenzile_utilizator (
    id_amenda NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_utilizator NUMBER,
    isbn VARCHAR2(20),
    data_amenda DATE,
    suma_amenda NUMBER,
    descriere VARCHAR2(255)
);



